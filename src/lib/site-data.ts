// Build a deterministic 52-point weekly sparkline
// start → end with realistic noise; high52w/low52w are the actual range extremes
function make52w(start: number, end: number, noise: number): number[] {
  const out: number[] = [];
  for (let i = 0; i < 52; i++) {
    const t = i / 51;
    const base = start + (end - start) * t;
    // simple sin-wave shaped path with noise
    const drift = Math.sin(t * Math.PI * 2.4) * noise * 0.6;
    const rand = (((i * 1103515245 + 12345) >>> 0) % 1000) / 1000 - 0.5;
    out.push(Math.round((base + drift + rand * noise * 0.5) * 10) / 10);
  }
  out[51] = end; // anchor last point to current price
  return out;
}

