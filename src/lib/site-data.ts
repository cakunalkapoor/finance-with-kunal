// --- Heatmap builder -------------------------------------------------------
// Deterministic pseudo-random so values are identical on server & client
// (avoids React hydration mismatches). Seeded from the ticker string.
function seededChange(seed: string): number {
  let h = 2166136261;
  for (let i = 0; i < seed.length; i++) {
    h ^= seed.charCodeAt(i);
    h = Math.imul(h, 16777619);
  }
  const r = ((h >>> 0) % 100000) / 100000; // 0..1
  return Math.round((r * 7.6 - 3.4) * 100) / 100; // ~ -3.4% .. +4.2%
}

// items: [ticker, weight] tuples
function buildSector(name: string, items: [string, number][]): HeatmapSector {
  const children = items.map(([ticker, value]) => ({
    name: ticker,
    ticker,
    value,
    change: seededChange(ticker),
  }));
  const totalW = children.reduce((s, c) => s + c.value, 0);
  const wChange =
    children.reduce((s, c) => s + c.change * c.value, 0) / (totalW || 1);
  return {
    name,
    value: Math.round(totalW * 10) / 10,
    change: Math.round(wChange * 100) / 100,
    children,
  };
}

